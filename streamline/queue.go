package streamline

import (
	"tomaskala.com/mapgen/field"
	"tomaskala.com/mapgen/vector"
)

type Family struct {
	grid  *Grid
	lines *[]Streamline
	sel   field.EigenSelector
}

type Item struct {
	p        vector.Vec2
	self     Family
	other    Family
	priority float64
}

type PriorityQueue []Item

func (pq PriorityQueue) Len() int {
	return len(pq)
}

func (pq PriorityQueue) Less(i, j int) bool {
	// Max heap.
	return pq[i].priority > pq[j].priority
}

func (pq PriorityQueue) Swap(i, j int) {
	pq[i], pq[j] = pq[j], pq[i]
}

func (pq *PriorityQueue) Push(x any) {
	item := x.(Item)
	*pq = append(*pq, item)
}

func (pq *PriorityQueue) Pop() any {
	old := *pq
	n := len(old)
	item := old[n-1]
	*pq = old[0 : n-1]
	return item
}
